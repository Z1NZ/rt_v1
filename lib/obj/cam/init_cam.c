#include "rt.h"

void	init_cam(t_env *e)
{
	e->cam.org.x = 0;
	e->cam.org.y = -2;
	e->cam.org.z = 0;
	e->cam.angle.x = 0;
	e->cam.angle.y = 0;
	e->cam.angle.z = 0;
}
